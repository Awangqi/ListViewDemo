public class Bean {

    private String name;
    private String Ambo;
    private boolean isChecked;




    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAmbo() {
        return Ambo;
    }

    public void setAmbo(String ambo) {
        Ambo = ambo;
    }

    public boolean getChecked() {
        return isChecked;
    }

    public void setChecked(boolean checked) {
        isChecked = checked;
    }
}
